        public override bool ValidateUser(string username, string password)
        {
            try
            {
                Login(username, password);
                return true;
            }
            catch (Exception exc)
            {
            }

            return false;
        }

        public void Login(string username, string password)
        {
            UsersController usersController = new UsersController();
            string hashedPassword =
                 FormsAuthentication.HashPasswordForStoringInConfigFile(password,"SHA1");
            usersController.GetByUserName(username).password.Equals(hashedPassword);
        }

        public void Logout()
        {
            Logout(false, string.Empty);
        }

        public void Logout(bool redirectToLoginPage)
        {
            Logout(redirectToLoginPage, string.Empty);
        }

        public void Logout(string extraQueryString)
        {
            Logout(true, extraQueryString);
        }

        public void Logout(bool redirectToLoginPage, string extraQueryString)
        {
            FormsAuthentication.SignOut();

            if (redirectToLoginPage)
            {
                System.Web.HttpContext.Current.Response.Clear();
                System.Web.HttpContext.Current.Response.Redirect(FormsAuthentication.LoginUrl, true);
            }
        }

        public static AdicomMembershipProvider Current
        {
            get
            {
                foreach (MembershipProvider provider in Membership.Providers)
                    if (provider is AdicomMembershipProvider) return provider as AdicomMembershipProvider;

                return null;
            }
        }

